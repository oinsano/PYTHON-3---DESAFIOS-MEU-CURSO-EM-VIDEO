#DECIFRANDO UM VARIAVEL
x=input('Digite algo: ')
print('é um digito?', x.isdigit())
print('é um alfabetico?',x.isalpha())
print('é um alfanumerico?',x.isalnum())
print('é um printavel?',x.isprintable())
print('é um sla?',x.isascii())
print('é deciamal?',x.isdecimal())
print('esta minusculo?',x.islower())
print('é um espaco?',x.isspace())
print('é um titulo?',x.istitle())
print('esta maiusculo?',x.isupper())

